function showBlog() {
    $('#Blog').fadeIn(500);
    $('#Portfolio').fadeOut(500);
    $('#Banner').fadeOut(500);
    $('#More').fadeOut(500);
}

function showPortfolio() {
    $('#Blog').fadeOut(500);
    $('#Portfolio').fadeIn(500);
    $('#Banner').fadeIn(500);
    $('#More').fadeOut(500);

}

